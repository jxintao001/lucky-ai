    public function resetPhone(Request $request){
        $inputs = $request->all();

        $validator = Validator::make($inputs,[
            'phone' => 'required',
            'code' => 'required',
        ]);

        if($validator->fails()){
            return $this->response->error($validator->errors(), 400);
        }

        //验证短信
        $valid = $this->checkMsg($request->input('phone'), $request->input('code'));
        if(!$valid){
            return $this->response->error('新手机短信验证码错误！', 400);
        }

        return $this->response->array(['message'=>'success', 'status_code'=>200]);
    }

    protected function checkMsg($phone, $code){
        $url = "http://bee.feixiong.tv/Api/Base/checkMsgCode/phone/$phone/code/$code";
        $client = new Client();
        $response = $client->request('get', $url, [])->getBody()->getContents();
        $response = \GuzzleHttp\json_decode($response);
        if($response && $response->status == 200){
            return true;
        }else{
            return false;
        }
    }

    /**
     * @param $url 请求网址
     * @param bool $params 请求参数
     * @param int $ispost 请求方式
     * @param int $https https协议
     * @return bool|mixed
     */
    public static function curl($url, $params = false, $ispost = 0, $https = 0)
    {
        $httpInfo = array();
        $ch = curl_init();
        curl_setopt($ch, CURLOPT_HTTP_VERSION, CURL_HTTP_VERSION_1_1);
        curl_setopt($ch, CURLOPT_USERAGENT, 'Mozilla/5.0 (Windows NT 10.0; WOW64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/41.0.2272.118 Safari/537.36');
        curl_setopt($ch, CURLOPT_CONNECTTIMEOUT, 30);
        curl_setopt($ch, CURLOPT_TIMEOUT, 30);
        curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
        if ($https) {
            curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, FALSE); // 对认证证书来源的检查
            curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, FALSE); // 从证书中检查SSL加密算法是否存在
        }
        if ($ispost) {
            curl_setopt($ch, CURLOPT_POST, true);
            curl_setopt($ch, CURLOPT_POSTFIELDS, $params);
            curl_setopt($ch, CURLOPT_URL, $url);
        } else {
            if ($params) {
                if (is_array($params)) {
                    $params = http_build_query($params);
                }
                curl_setopt($ch, CURLOPT_URL, $url . '?' . $params);
            } else {
                curl_setopt($ch, CURLOPT_URL, $url);
            }
        }

        $response = curl_exec($ch);

        if ($response === FALSE) {
            //echo "cURL Error: " . curl_error($ch);
            return false;
        }
        $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
        $httpInfo = array_merge($httpInfo, curl_getinfo($ch));
        curl_close($ch);
        return $response;
    }
